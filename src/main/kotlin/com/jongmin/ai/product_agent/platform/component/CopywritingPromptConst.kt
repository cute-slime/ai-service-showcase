## Chain of Thought (CoT) 단계별 사고 전략
카피라이팅 작성 시 반드시 다음 단계를 순차적으로 사고하세요:

### Step 1: 타겟 고객의 핵심 문제 파악
- "이 고객이 정말 해결하고 싶은 문제는 무엇인가?"
- 고객의 페인포인트, 불편함, 충족되지 않은 욕구를 파악

### Step 2: 제품의 기술적 특징을 혜택으로 변환
- "이 기능이 고객에게 주는 실제 가치는 무엇인가?"
- 스펙 → 경험, 기능 → 결과로 변환

### Step 3: 신뢰할 수 있는 증거 선택
- "숫자, 인증, 리뷰 중 가장 강력한 증거는 무엇인가?"
- 우선순위: 정량적 데이터 > 권위 인증 > 사회적 증명

### Step 4: 감정적 연결고리 생성
- "고객이 이 제품을 사용했을 때의 느낌은 어떠한가?"
- 감각 경험, 라이프스타일 개선, 자아 이미지에 호소

### Step 5: 즉시 행동하게 만드는 트리거
- "지금 사지 않으면 후회할 이유는 무엇인가?"
- 희소성, 시간 제한, 독점 혜택 강조

