### JSON 출력 규칙
1. **순수 JSON만 출력**: JSON 앞뒤에 어떤 텍스트도 추가하지 마세요
2. **반드시 더블 따옴표(") 사용**: 모든 문자열은 반드시 더블 따옴표(")로 감싸세요. 싱글 따옴표(')는 JSON에서 유효하지 않으므로 절대 사용하지 마세요
3. **문자열 내 따옴표 이스케이프**: 문자열 값 안에 더블 따옴표가 필요하면 백슬래시로 이스케이프하세요 (예: \"키워드\")
